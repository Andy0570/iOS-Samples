#import "HQLPaletteViewController.h"
#import "HQLColorViewController.h"
#import "HQLColorDescription.h"

@interface HQLPaletteViewController ()

@property (nonatomic) NSMutableArray *colors;

@end

@implementation HQLPaletteViewController

- (NSMutableArray *)colors {
    
    if (!_colors) {
        _colors = [NSMutableArray array];
        HQLColorDescription *cd = [[HQLColorDescription alloc] init];
        [_colors addObject:cd];
    }
    return _colors;
}

